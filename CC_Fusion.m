function X = CC_Fusion(X1,P1,X2,P2)
X(1) = X1(1)/abs(P1(1,1)) + X2(1)/abs(P2(1,1));
X(1) = X(1)/(1/abs(P1(1,1)) + 1/abs(P2(1,1)));

X(2) = X1(2)/abs(P1(2,2)) + X2(2)/abs(P2(2,2));
X(2) = X(2)/(1/abs(P1(2,2)) + 1/abs(P2(2,2)));

X(3) = X1(3)/abs(P1(3,3)) + X2(3)/abs(P2(3,3));
X(3) = X(3)/(1/abs(P1(3,3)) + 1/abs(P2(3,3)));

X(4) = X1(4)/abs(P1(4,4)) + X2(4)/abs(P2(4,4));
X(4) = X(4)/(1/abs(P1(4,4)) + 1/abs(P2(4,4)));
end